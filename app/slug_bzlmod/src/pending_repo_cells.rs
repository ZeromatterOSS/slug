        let result2 = ModuleExtensionResult::new(
            Arc::from("@@rules_go//go:deps.bzl%go_deps"),
            "sha256-go".to_owned(),
            specs2,
            "",
        );
        let defs2 = build_extension_cells(&result2).unwrap();

